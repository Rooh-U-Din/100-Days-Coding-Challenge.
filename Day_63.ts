// Type annotation
let str:string="Hello World!";
let nbr:number=42;
console.log(`My String is ${str}.`);
console.log(`My Number is ${nbr}.`);

// Another way to write this code is  as follows

let str2:string;
let nbr2:number;

str2 = "Hello World";
nbr2=42;

console.log(`My String is ${str2}.`);
console.log(`My Number is ${nbr2}.`);
console.log("-------------------------------------------------");
//Dynamic type determination

let greeting ="Hello World";
